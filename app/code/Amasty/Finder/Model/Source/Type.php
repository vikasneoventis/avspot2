namespace Amasty\Finder\Model\Source;


class Type implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        return [['value' => 1, 'label' => __('At least one finder value is selected')], ['value' => 0, 'label' => __('All finder values are selected')]];
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return [0 => __('All finder values are selected'), 1 => __('At least one finder value is selected')];
    }
}
